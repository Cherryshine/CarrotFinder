    def sort_items_by_price(self):
        """가격순으로 항목 정렬"""
        # 현재 활성화된 탭의 트리뷰 가져오기
        current_tab = self.notebook.select()
        if self.notebook.index(current_tab) == 0:
            tree = self.main_tree
        else:
            tree = self.search_tree
        
        if not tree:
            return

        items = []
        for item in tree.get_children():
            values = tree.item(item)["values"]
            try:
                price_str = values[4].replace('원', '').replace(',', '')
                price = float(price_str)
                items.append((price, item))
            except (ValueError, IndexError) as e:
                print(f"가격 변환 오류: {e}")
                continue

        items.sort()

        for idx, (_, item) in enumerate(items):
            tree.move(item, '', idx)
            values = list(tree.item(item)["values"])
            values[0] = idx + 1
            tree.item(item, values=values)

    def sort_items_by_chat(self):
        """채팅순으로 항목 정렬"""
        # 현재 활성화된 탭의 트리뷰 가져오기
        current_tab = self.notebook.select()
        if self.notebook.index(current_tab) == 0:
            tree = self.main_tree
        else:
            tree = self.search_tree
        
        if not tree:
            return

        items = []
        for item in tree.get_children():
            values = tree.item(item)["values"]
            try:
                chat_count = int(values[5]) if values[5] else 0
                items.append((-chat_count, item))  # 음수로 저장하여 내림차순 정렬
            except (ValueError, IndexError) as e:
                print(f"채팅수 변환 오류: {e}")
                continue

        items.sort()

        for idx, (_, item) in enumerate(items):
            tree.move(item, '', idx)
            values = list(tree.item(item)["values"])
            values[0] = idx + 1
            tree.item(item, values=values)

