    @lru_cache(maxsize=100)
    def _fetch_region_items(self, region_code, search_query, min_price, max_price):
        """캐시된 지역별 검색 결과 반환"""
        try:
            # URL 구성
            price_param = ""
            if min_price and max_price:
                price_param = f"&price={min_price}__{max_price}"
            elif min_price:
                price_param = f"&price={min_price}__"
            elif max_price:
                price_param = f"&price=__{max_price}"

            search_query = urllib.parse.quote(search_query, encoding='utf-8', safe='')
            url = f"https://www.daangn.com/kr/buy-sell/?in={region_code}&only_on_sale=true&search={search_query}{price_param}&sort_by=recent"
            
            headers = {
                "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36 (KHTML, like Gecko) Chrome/120.0.0.0 Safari/537.36",
                "Accept": "text/html,application/xhtml+xml,application/xml;q=0.9,image/avif,image/webp,image/apng,*/*;q=0.8",
                "Accept-Language": "ko-KR,ko;q=0.9,en-US;q=0.8,en;q=0.7"
            }
            
            response = self.session.get(url, headers=headers, timeout=10)
            response.encoding = 'utf-8'
            
            if response.status_code == 200:
                content = response.text
                remix_context_start = content.find('window.__remixContext = ')
                if remix_context_start != -1:
                    start_index = remix_context_start + len('window.__remixContext = ')
                    end_index = content.find(';</script>', start_index)
                    if end_index != -1:
                        json_str = content[start_index:end_index]
                        remix_context = json.loads(json_str)
                        loader_data = remix_context.get('state', {}).get('loaderData', {})
                        route_key = 'routes/kr.buy-sell._index'
                        if route_key in loader_data:
                            products = loader_data[route_key].get('allPage', {}).get('fleamarketArticles', [])
                            # 지역 이름 추가
                            region_name = region_code.split('-')[0]
                            for product in products:
                                product['region_name'] = region_name
                            return products
            return []
        except Exception as e:
            print(f"Error fetching items for region {region_code}: {str(e)}")
            return []

