class ScrollableImageTreeview(ttk.Treeview):
    def __init__(self, parent, **kwargs):
        style = ttk.Style()
        style.configure('Treeview', rowheight=160)
        
        super().__init__(parent, **kwargs)
        self.images = {}
        self.image_load_queue = {}  # 이미지 로드 큐 추가

    def insert_with_image(self, parent, index, image_url, **kwargs):
        item = self.insert(parent, index, **kwargs)
        if image_url:
            # 비동기로 이미지 로드
            threading.Thread(
                target=self._async_load_image,
                args=(item, image_url, (83, 83)),  # 이미지 크기 10% 증가 (75,75 -> 83,83)
                daemon=True
            ).start()
        return item

    def _async_load_image(self, item, url, size=(83, 83)):
        """이미지를 비동기적으로 로드"""
        try:
            response = requests.get(url, timeout=5)
            img = Image.open(BytesIO(response.content))
            img = img.resize(size, Image.Resampling.LANCZOS)
            # UI 업데이트는 메인 스레드에서 수행
            self.after(0, self._update_image, item, img)
        except Exception as e:
            print(f"이미지 로드 실패: {e}")

    def _update_image(self, item, img):
        """메인 스레드에서 이미지 업데이트"""
        try:
            photo = ImageTk.PhotoImage(img)
            self.images[item] = photo
            self.item(item, image=photo)
        except Exception as e:
            print(f"이미지 업데이트 실패: {e}")

    def delete(self, *items):
        """항목 삭제 시 이미지 캐시도 정리"""
        for item in items:
            if item in self.images:
                del self.images[item]
        super().delete(*items)

