class LicenseValidator:
    def __init__(self):
        self.key = b'dL-KXsbXFQGheP6ckh1OlZE4YWxOGxjqh0ZsO-2JYpM='
        self.cipher = Fernet(self.key)
        self.license_file = "license.dat"

    def validate_license_key(self, license_key):
        try:
            # base64 디코딩 후 복호화
            decoded = base64.b64decode(license_key)
            decrypted = self.cipher.decrypt(decoded)
            plan = decrypted.decode()
            
            # 라이센스 정보 저장
            self._save_license_info(plan)
            return True
        except:
            return False

    def _save_license_info(self, plan):
        start_date = datetime.now()
        
        if plan == "cherrypermanent":
            end_date = None
        elif plan == "cherry20sec":  # 20초 테스트 요금제 추가
            end_date = start_date + timedelta(seconds=20)
        else:
            months = int(plan.replace("cherry", "").replace("month", ""))
            end_date = start_date + timedelta(days=30*months)
        
        license_data = {
            "start_date": start_date.isoformat(),
            "end_date": end_date.isoformat() if end_date else None,
            "plan": plan
        }
        
        # 라이센스 정보 암호화
        encrypted_data = self.cipher.encrypt(json.dumps(license_data).encode())
        
        with open(self.license_file, "wb") as f:
            f.write(encrypted_data)

    def check_license_validity(self):
        try:
            if not os.path.exists(self.license_file):
                return False
                
            with open(self.license_file, "rb") as f:
                encrypted_data = f.read()
                
            decrypted_data = self.cipher.decrypt(encrypted_data)
            license_data = json.loads(decrypted_data)
            
            if license_data["end_date"] is None:  # 영구 라이센스
                return True
                
            end_date = datetime.fromisoformat(license_data["end_date"])
            return datetime.now() <= end_date
            
        except:
            return False

