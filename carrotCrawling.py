    def search_in_results(self):
        """결과 내 검색 수행"""
        search_text = self.result_search_var.get().lower().strip()
        if not search_text:
            return

        print(f"\n[결과내 검색] 검색어: '{search_text}'")
        
        # 이전 검색 결과 확인 (item_data에 있는 항목)
        print(f"[결과내 검색] item_data에 저장된 항목 수: {len(self.item_data)}")
        if not self.item_data:
            messagebox.showinfo("알림", "검색 결과가 없습니다. 먼저 기본 검색을 실행하세요.")
            return
        
        # 기존 결과내 검색 탭이 있다면 제거
        if self.search_tab:
            self.notebook.forget(self.search_tab)

        # 메인 검색어 가져오기
        main_search_text = self.search_var.get()
        tab_text = f'"{main_search_text}"결과 내 "{search_text}" 검색'

        # 새로운 결과내 검색 탭 생성
        self.search_tab = tk.Frame(self.notebook)
        self.notebook.add(self.search_tab, text=tab_text)
        
        # 새로운 트리뷰 생성
        table_frame = tk.Frame(self.search_tab)
        table_frame.pack(fill=tk.BOTH, expand=True)
        self.search_tree = self.create_treeview(table_frame)
        
        # 결과내 검색 상태 활성화
        self.is_search_tab_active = True
        
        # 기존 검색 결과에서 필터링하여 새 트리뷰에 추가
        idx = 1
        found_items = 0
        
        # item_data를 기반으로 검색
        print(f"[결과내 검색] item_data의 키 목록: {list(self.item_data.keys())}")
        for item_id, item_info in self.item_data.items():
            try:
                title = item_info.get('title', '').lower()
                print(f"[결과내 검색] 항목 ID: {item_id}, 제목: '{title}'")
                
                # 제목에서 검색어 확인
                print(f"[결과내 검색] 검색어 '{search_text}'와 비교 중...")
                
                if search_text in title:
                    print(f"[결과내 검색] 일치함! 검색어: '{search_text}', 제목: '{title}'")
                    
                    # 결과내 검색을 위한 독립적인 item_id 생성
                    search_item_id = f"search_{item_id}"
                    
                    # 이미지 URL 가져오기
                    image_url = item_info.get('image_url', '')
                    
                    # 값 설정
                    price_display = self._format_price(item_info.get('price', 0))
                    chat_count = item_info.get('chat_count', 0)
                    
                    # 새 아이템 추가
                    new_item = self.search_tree.insert_with_image(
                        "", tk.END,
                        text="",
                        image_url=image_url,
                        tags=(search_item_id,),
                        values=(idx, title, item_info.get('seller', ''), item_info.get('region', ''), price_display, chat_count)
                    )
                    
                    # 독립적인 저장소에 데이터 보관
                    self.search_items_data[search_item_id] = item_info.copy()
                    print(f"[결과내 검색] 데이터 복사 성공: {item_id} -> {search_item_id}")
                    
                    found_items += 1
                    idx += 1
                else:
                    print(f"[결과내 검색] 일치하지 않음: '{search_text}'는 '{title}'에 없음")
            except Exception as e:
                print(f"[결과내 검색] 항목 처리 중 오류 발생: {e}")
                continue

        # 검색 결과 개수 로그 추가
        print(f"[결과내 검색] 검색 결과: {found_items}개 항목 발견")
        
        if found_items == 0:
            messagebox.showinfo("알림", f"검색어 '{search_text}'에 일치하는 항목이 없습니다.")
            self.notebook.forget(self.search_tab)
            self.search_tab = None
            self.search_tree = None
            return
        
        # 채팅수 컬럼 표시 상태 업데이트
        if self.collect_chat_count_var.get():
            self.search_tree["displaycolumns"] = ("번호", "제목", "판매자", "지역", "가격", "채팅")
        else:
            self.search_tree["displaycolumns"] = ("번호", "제목", "판매자", "지역", "가격")
        
