    def _process_search(self):
        """백그라운드에서 검색 처리"""
        try:
            search_keyword = self.search_var.get()
            min_price = self.min_price.get().strip().replace(',', '')
            max_price = self.max_price.get().strip().replace(',', '')

            # 검색할 지역 코드 결정
            region_codes = []
            if self.nationwide_var.get():
                for sido in self.sido_mapping.values():
                    for region_code in sido.values():
                        # set인 경우 모든 코드를 추가
                        if isinstance(region_code, set):
                            region_codes.extend(list(region_code))
                        else:
                            region_codes.append(region_code)
            else:
                if isinstance(self.selected_region_code, set):
                    region_codes.extend(list(self.selected_region_code))
                else:
                    region_codes = [self.selected_region_code]

            # 검색 진행률 초기화
            total_regions = len(region_codes)
            processed_regions = 0

            # 검색 상태 초기화
            self.progress_var.set(0)
            self.progress_label.config(text="검색 준비 중...")
            self.root.update_idletasks()

            # 병렬 검색 실행 (작업자 수 증가)
            with concurrent.futures.ThreadPoolExecutor(max_workers=10) as executor:
                # 지역 코드를 청크로 나누어 처리
                chunk_size = 20  # 한 번에 처리할 지역 수
                for i in range(0, len(region_codes), chunk_size):
                    if self.stop_event.is_set():
                        break

                    chunk = region_codes[i:i + chunk_size]
                    futures = []
                    for region_code in chunk:
                        future = executor.submit(
                            self._fetch_region_items,
                            region_code,
                            search_keyword,
                            min_price,
                            max_price
                        )
                        futures.append(future)

                    # 청크 단위로 결과 처리
                    for future in concurrent.futures.as_completed(futures):
                        if self.stop_event.is_set():
                            break

                        processed_regions += 1
                        progress = (processed_regions / total_regions) * 100
                        self.progress_var.set(progress)
                        self.progress_label.config(text=f"검색 진행 중... ({progress:.1f}%)")
                        self.root.update_idletasks()

                        try:
                            products = future.result()
                            if products:
                                self._process_products(products)
                        except Exception as e:
                            print(f"Error processing search results: {str(e)}")

                    # 과도한 요청 방지를 위한 짧은 대기
                    time.sleep(0.5)

        finally:
            self.is_searching = False
            self.search_button_text.set("검색하기")
            self.progress_label.config(text="검색 완료")
            
            # 검색 완료 후 결과내 검색 버튼 활성화
            if self.items:
                self.root.after(0, lambda: self.result_search_button.config(state=tk.NORMAL))
            
            if not self.items:
                messagebox.showinfo("알림", "검색 결과가 없습니다.")

    def _process_products(self, products):
        """검색된 상품들을 처리하여 트리뷰에 추가"""
        current_items = self.main_tree.get_children()
        start_idx = len(current_items) + 1

        for idx, product in enumerate(products, start_idx):
            if self.stop_event.is_set():
                return

            try:
                title = product.get('title', '').strip()
                price = product.get('price', 0)
                seller = product.get('user', {}).get('nickname', '').strip()
                description = product.get('content', '').strip()
                image_url = product.get('thumbnail', '')
                url = product.get('href', '')
                region_name = product.get('region_name', '').strip()

                if url and not url.startswith('http'):
                    url = f"https://www.daangn.com{url}"

                chat_count = 0
                if self.collect_chat_count_var.get():
                    chat_count = self.get_chat_count_with_session(url)

                item_id = f"item_{idx}_{len(self.items)}"
                self.item_data[item_id] = {
                    'title': title,
                    'price': price,
                    'seller': seller,
                    'region': region_name,
                    'url': url,
                    'description': description,
                    'image_url': image_url,
                    'chat_count': chat_count
                }

                # 가격 포맷팅
                price_display = self._format_price(price)

                # 트리뷰에 아이템 추가
                item = self.main_tree.insert_with_image(
                    "", tk.END,
                    text="",
                    image_url=image_url,
                    tags=(item_id,),
                    values=(idx, title, seller, region_name, price_display, chat_count)
                )

                self.items.append(item_id)
                
                # 채팅수 컬럼 표시 상태 업데이트
                if self.collect_chat_count_var.get():
                    self.main_tree["displaycolumns"] = ("번호", "제목", "판매자", "지역", "가격", "채팅")
                else:
                    self.main_tree["displaycolumns"] = ("번호", "제목", "판매자", "지역", "가격")

                self.root.update_idletasks()

            except Exception as e:
                print(f"Error processing product: {str(e)}")
                continue

    def _format_price(self, price):
        """가격을 포맷팅"""
        if not price:
            return "가격없음"
        try:
            price_str = str(int(float(price)))
            formatted_price = ""
            for i in range(len(price_str)-1, -1, -1):
                if i != len(price_str)-1 and (len(price_str)-i-1) % 3 == 0:
                    formatted_price = "," + formatted_price
                formatted_price = price_str[i] + formatted_price
            return formatted_price + "원"
        except:
            return "가격없음"

